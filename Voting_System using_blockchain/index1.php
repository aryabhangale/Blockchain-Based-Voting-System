      <h2>Electronic Voting System using Blockchain</h2>
     
     
    
      <p align="justify">

Voting is a method for a group of individuals to make a collective decision or voice their cumulative opinion to arrive at a consensus. The results of a vote can have far reaching consequences due to which it is imperative to maintain integrity by ensuring that there is no scope for fraud or cheating to occur while the votes are being cast. It is highly crucial that voting is carried out in a fair and just manner.<br>
This Electronic Voting System has been developed to help eliminate any chance of tampering and improve the reliability and dependability of a voting system.<br>
This system consists of two entities namely; The Admin and The Voter (The User). 
Once the Voter has logged in to the system using their valid username and password, they can view the upcoming elections and the candidates contesting the election. Users can also view results once the elections have concluded. This system also shows user the elections that they have participated in so far. <br>
On the other side of the application, the admin can view the list of candidates contesting, the list of voters, and the list of elections.<br>
Since this system maintains the data using blockchain, it is highly dependable and can be easily scanned to check for signs of tampering and malpractice.
 

